package com.pojo;

public class Book {

  private long id;
  private String name;
  private String author;
  private long language;
  private long category;
  private long cdrom;
  private long commend;
  private String content;
  private double price;
  private String onSaleTime;
  private long goodPrice;
  private String publishName;
  private String publishAddress;
  private long bookNum;
  private String pictureURL;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }


  public String getAuthor() {
    return author;
  }

  public void setAuthor(String author) {
    this.author = author;
  }


  public long getLanguage() {
    return language;
  }

  public void setLanguage(long language) {
    this.language = language;
  }


  public long getCategory() {
    return category;
  }

  public void setCategory(long category) {
    this.category = category;
  }


  public long getCdrom() {
    return cdrom;
  }

  public void setCdrom(long cdrom) {
    this.cdrom = cdrom;
  }


  public long getCommend() {
    return commend;
  }

  public void setCommend(long commend) {
    this.commend = commend;
  }


  public String getContent() {
    return content;
  }

  public void setContent(String content) {
    this.content = content;
  }


  public double getPrice() {
    return price;
  }

  public void setPrice(double price) {
    this.price = price;
  }


  public String getOnSaleTime() {
    return onSaleTime;
  }

  public void setOnSaleTime(String onSaleTime) {
    this.onSaleTime = onSaleTime;
  }


  public long getGoodPrice() {
    return goodPrice;
  }

  public void setGoodPrice(long goodPrice) {
    this.goodPrice = goodPrice;
  }


  public String getPublishName() {
    return publishName;
  }

  public void setPublishName(String publishName) {
    this.publishName = publishName;
  }


  public String getPublishAddress() {
    return publishAddress;
  }

  public void setPublishAddress(String publishAddress) {
    this.publishAddress = publishAddress;
  }


  public long getBookNum() {
    return bookNum;
  }

  public void setBookNum(long bookNum) {
    this.bookNum = bookNum;
  }

  public String getPictureURL() {
    return pictureURL;
  }

  public void setPictureURL(String pictureURL) {
    this.pictureURL = pictureURL;
  }
}
